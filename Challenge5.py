#Declare and initialize the variable 'temperature' in Celcius
temperature = 25.5 

#Conversion to Kelvin
kelvin = temperature + 273

#Print the temperature in Celcius and Kelvin
print(f"Celsius - {temperature}")
print(f"Kelvin - {kelvin}")


